void EPD_Reset(void)
{
  	LL_mDelay(RESET_TIME);
  	EPD_RST_UP;
  	LL_mDelay(RESET_TIME);
  	EPD_RST_DOWN;
  	LL_mDelay(RESET_TIME);
}
void EPD_Init(void)
{
//-------------- Driver Output control
  EPD_SendCommand(0x01);
  EPD_SendData(0xc7);
  EPD_SendData(0x00);
  EPD_SendData(0x00);
//------------------
//--------------Gate Driving voltage Control
  EPD_SendCommand(0x03);
  EPD_SendData(0xEA);
//--------------
//--------------Source Driving	voltage Control
  EPD_SendCommand(0x04);
  EPD_SendData(0x0A);
//--------------
//--------------Set dummy line period
  EPD_SendCommand(0x3A);
  EPD_SendData(0x02);
//--------------
//--------------Set Gate line width
  EPD_SendCommand(0x3B);
  EPD_SendData(0x08);
//--------------
//--------------Border Waveform Control
  EPD_SendCommand(0x3C);
  EPD_SendData(0x71);
//--------------
//--------------Data Entry mode setting
  EPD_SendCommand(0x11);
  EPD_SendData(0x03);
//--------------
//--------------Set RAM X - address Start / End position
  EPD_SendCommand(0x44);
  EPD_SendData(0x00);//xDot -start
  EPD_SendData(0x18);//xDot/8 -end
//--------------Set RAM X - address Start / End position
  EPD_SendCommand(0x45);
  EPD_SendData(0x00); //yDot/8 start
  EPD_SendData(0x00); //yDot/8 start
  EPD_SendData(0xC7); //yDot%256
  EPD_SendData(0x00); //yDot/256
//--------------
//--------------Set RAM X address counter
  EPD_SendCommand(0x4E);
  EPD_SendData(0x00);
//--------------
//--------------Set RAM X address counter
  EPD_SendCommand(0x4F);
  EPD_SendData(0x00);
  EPD_SendData(0x00);
}
void EPD_SendCommand(uint8_t command)
{
	EPD_DC_DOWN;
	HAL_SPI_Transmit(&hspi1, &command, 1, 1);
}
void EPD_SendData(uint8_t command)
{
	EPD_DC_UP;
	HAL_SPI_Transmit(&hspi1, &command, 1, 1);
	EPD_DC_DOWN;
}
void Busy()
{
	//Ждем когда дисплей отпустит пин.
	while( LL_GPIO_ReadInputPort(BUSY_GPIO_Port)&BUSY_Pin )
	{
		LL_mDelay(100);
	}
}
